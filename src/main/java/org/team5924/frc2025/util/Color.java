package org.team5924.frc2025.util;

/** Add your docs here. */
public class Color {

  private int red;
  private int blue;
  private int green;

  public Color(int red, int blue, int green) {
    this.red = red;
    this.green = green;
    this.blue = blue;
  }

  public int getRed() {
    return red;
  }

  public int getGreen() {
    return green;
  }

  public int getBlue() {
    return blue;
  }
}
